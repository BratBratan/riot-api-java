package dto;

public class TeamMemberInfo {
	
	private long inviteDate, joinDate;
	private long playerId;
	private String status;
	
	public long getInviteDate() {
		return inviteDate;
	}
	
	public long getJoinDate() {
		return joinDate;
	}
	
	public long getPlayerId() {
		return playerId;
	}
	
	public String getStatus() {
		return status;
	}
	
	
	
}
